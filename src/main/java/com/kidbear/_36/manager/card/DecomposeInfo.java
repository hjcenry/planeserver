package com.kidbear._36.manager.card;

public class DecomposeInfo {
	private int pickTimes;
	private int decomposeTimes;
	private long resettime;

	public int getPickTimes() {
		return pickTimes;
	}

	public void setPickTimes(int pickTimes) {
		this.pickTimes = pickTimes;
	}

	public int getDecomposeTimes() {
		return decomposeTimes;
	}

	public void setDecomposeTimes(int decomposeTimes) {
		this.decomposeTimes = decomposeTimes;
	}

	public long getResettime() {
		return resettime;
	}

	public void setResettime(long resettime) {
		this.resettime = resettime;
	}

}
