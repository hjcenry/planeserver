package com.kidbear._36.template;

public class Minju {
	private int MinjuLv;
	private int ScienceCostCoin;
	private int ScienceCostFood;
	private int CostTime;
	private int GetCoinhour;
	private int MinjuUpLimit;

	public int getMinjuLv() {
		return MinjuLv;
	}

	public void setMinjuLv(int minjuLv) {
		MinjuLv = minjuLv;
	}

	public int getScienceCostCoin() {
		return ScienceCostCoin;
	}

	public void setScienceCostCoin(int scienceCostCoin) {
		ScienceCostCoin = scienceCostCoin;
	}

	public int getScienceCostFood() {
		return ScienceCostFood;
	}

	public void setScienceCostFood(int scienceCostFood) {
		ScienceCostFood = scienceCostFood;
	}

	public int getCostTime() {
		return CostTime;
	}

	public void setCostTime(int costTime) {
		CostTime = costTime;
	}

	public int getMinjuUpLimit() {
		return MinjuUpLimit;
	}

	public void setMinjuUpLimit(int minjuUpLimit) {
		MinjuUpLimit = minjuUpLimit;
	}

	public int getGetCoinhour() {
		return GetCoinhour;
	}

	public void setGetCoinhour(int getCoinhour) {
		GetCoinhour = getCoinhour;
	}
}
