package com.kidbear._36.template;

public class CardExp {
	private int HeroLv;
	private long SubExp;
	private long SumExp;

	public long getSumExp() {
		return SumExp;
	}

	public void setSumExp(long sumExp) {
		SumExp = sumExp;
	}

	public long getSubExp() {
		return SubExp;
	}

	public void setSubExp(long subExp) {
		SubExp = subExp;
	}

	public int getHeroLv() {
		return HeroLv;
	}

	public void setHeroLv(int heroLv) {
		HeroLv = heroLv;
	}
}
